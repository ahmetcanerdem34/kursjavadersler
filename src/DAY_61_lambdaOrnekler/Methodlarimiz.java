package DAY_61_lambdaOrnekler;

public class Methodlarimiz {

	public static boolean tekMi(int x) {
		return x % 2 != 0;

	}

	public static boolean çiftMi(int x) {
		return x % 2 == 0;

	}

	public static void yazdir(int x) {

		System.out.print(x + " ");

	}
	// kare al methodu oluþturalým

	public static int kareAl(int x) {

		return x * x;
	}
	// küpünü al methodu oluþturalým
	
	public static int kupAl(int x) {
		
		return x*x*x;
		
	}

}
