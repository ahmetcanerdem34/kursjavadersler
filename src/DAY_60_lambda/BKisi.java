package DAY_60_lambda;

public class BKisi {
	
	private String isim;
	private int yas;
	
	public BKisi(String isim, int yas) {
		
		this.isim = isim;
		this.yas=yas;
	}

	public String getIsim() {
		return isim;
	}

	public void setIsim(String isim) {
		this.isim = isim;
	}

	public int getYas() {
		return yas;
	}

	public void setYas(int yas) {
		this.yas = yas;
	}

	
}
